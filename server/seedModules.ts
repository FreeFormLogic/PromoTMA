// Функция для создания модуля
function createModule(
  number: number,
  name: string,
  description: string,
  category: string,
  keyFeatures: string[],
  benefits: string,
  icon: string = "Component"
): Module {
  return {
    id: randomUUID(),
    number,
    name,
    description,
    category,
    icon,
    keyFeatures,
    benefits,
    isPopular: false
  };
}

